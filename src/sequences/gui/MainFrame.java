    private void lockSequenceChoice(boolean lock) {
        //locks
        jComboBoxSeqences.setEnabled(!lock);
        jButtonSelect.setEnabled(!lock);
        //unlocks
        jButtonShow.setEnabled(lock);
        jTextFieldMax.setEnabled(lock);
        jButtonChange.setEnabled(lock);
        jButtonReset.setEnabled(lock);
        jButtonSave.setEnabled(lock);
        jTextFieldDecompose.setEnabled(lock);
        jButtonDecompose.setEnabled(lock);
        jTextFieldSum.setEnabled(lock);
        jButtonSum.setEnabled(lock);
        if (lock) { //we lock the top
            jLabelMax.setText(Integer.toString(currentSq.getMax()));
        } else { //We lock the bottom
            jLabelMax.setText("---");
            jTextFieldMax.setText("");
            currentSq = null;
            jTextFieldDecompose.setText("");
            jTextFieldSum.setText("");
        }
    }

