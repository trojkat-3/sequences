    private Integer readIntFromTextField(JTextField tf){
        String valStr = tf.getText();
        int val;
        try {
            val = Integer.parseInt(valStr);
        } catch (NumberFormatException ex) {
            showError("Wrong value.");
            return null;
        }
        return val;
    }
    
    private void showError(String msg){
        JOptionPane.showMessageDialog(this, msg , "Error", JOptionPane.ERROR_MESSAGE);
    }
    
    private void showInfo(String msg){
        JOptionPane.showMessageDialog(this, msg , "Info", JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void jButtonChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonChangeActionPerformed
        Integer val=readIntFromTextField(jTextFieldMax);
        if (val==null){
            return;
        }
        currentSq.setMax(val);
        jLabelMax.setText(Integer.toString(currentSq.getMax()));
    }//GEN-LAST:event_jButtonChangeActionPerformed

